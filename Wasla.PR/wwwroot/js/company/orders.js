// SHOW ASSIGN ORDER TO DELEGATE (from delegates card or dashboard)
// ==============================
window.showAssignOrderToDelegate = function(delegateId) {
    const orders = getOrders().filter(o => o.status === 'created');
    if (orders.length === 0) {
        showToast('لا توجد طلبات جديدة للتعيين', 'error');
        return;
    }
    const select = document.getElementById('assignOrderSelect');
    if (!select) return;
    select.innerHTML = orders.map(o => `<option value="${o.id}">${o.orderNo} - ${o.customer} (${o.netAmount} ج.م)</option>`).join('');
    window.tempDelegateId = delegateId;
    window.showModal('assignOrderToDelegateModal');
};

