let currentLang = localStorage.getItem('company_lang') || 'ar';
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('company_lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = translations[lang][key];
            } else if (el.tagName === 'INPUT' && el.type !== 'submit') {
                if (!el.value || el.value === el.getAttribute('data-original-value')) {
                    el.value = translations[lang][key];
                    el.setAttribute('data-original-value', translations[lang][key]);
                }
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });
    document.querySelectorAll('.company-lang-btn, .lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) btn.classList.add('active');
        else btn.classList.remove('active');
    });
    // trigger dynamic updates
    if (typeof window.refreshOrdersTable === 'function') window.refreshOrdersTable();
    if (typeof window.renderDelegatesList === 'function') window.renderDelegatesList();
    if (typeof window.refreshFinancialStats === 'function') window.refreshFinancialStats();
    if (typeof window.refreshDashboard === 'function') window.refreshDashboard();
}

