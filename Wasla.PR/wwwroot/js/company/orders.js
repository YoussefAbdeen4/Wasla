// ==============================
// CONFIRM MODAL (global)
// ==============================
let confirmResolve = null;
window.showConfirmModal = function(message, title = 'تأكيد') {
    return new Promise((resolve) => {
        let modal = document.getElementById('globalConfirmModal');
        if (!modal) {
            modal = document.createElement('div');
            modal.id = 'globalConfirmModal';
            modal.className = 'company-modal';
            modal.innerHTML = `
                <div class="company-modal-content" style="max-width: 400px;">
                    <div class="company-modal-header">
                        <h3 id="confirmModalTitle">${title}</h3>
                        <button class="company-modal-close">&times;</button>
                    </div>
                    <div class="company-modal-body">
                        <p id="confirmModalMessage">${message}</p>
                    </div>
                    <div class="company-modal-footer">
                        <button class="company-btn company-btn-outline" id="confirmModalCancel">إلغاء</button>
                        <button class="company-btn company-btn-primary" id="confirmModalOk">تأكيد</button>
                    </div>
                </div>
            `;
            document.body.appendChild(modal);
            const closeBtn = modal.querySelector('.company-modal-close');
            const cancelBtn = modal.querySelector('#confirmModalCancel');
            const okBtn = modal.querySelector('#confirmModalOk');
            const closeHandler = () => {
                modal.classList.remove('show');
                if (confirmResolve) confirmResolve(false);
                confirmResolve = null;
            };
            closeBtn.addEventListener('click', closeHandler);
            cancelBtn.addEventListener('click', closeHandler);
            okBtn.addEventListener('click', () => {
                modal.classList.remove('show');
                if (confirmResolve) confirmResolve(true);
                confirmResolve = null;
            });
            modal.addEventListener('click', (e) => { if (e.target === modal) closeHandler(); });
        }
        document.getElementById('confirmModalTitle').innerText = title;
        document.getElementById('confirmModalMessage').innerText = message;
        confirmResolve = resolve;
        modal.classList.add('show');
    });
};

// ==============================
// CLOSE MODALS VIA data-modal-close (for external use)
// ==============================
function initModals() {
    document.querySelectorAll('[data-modal-close]').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const modalId = btn.getAttribute('data-modal-close');
            if (modalId) window.closeModal(modalId);
        });
    });
    document.querySelectorAll('.company-modal').forEach(modal => {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) modal.classList.remove('show');
        });
    });
}

