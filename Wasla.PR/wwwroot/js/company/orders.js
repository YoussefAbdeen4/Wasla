// ==============================
// STORAGE KEYS
// ==============================
const STORAGE_KEYS = {
    ORDERS: 'app_orders',
    AGENTS: 'app_agents',
    TRANSACTIONS: 'app_transactions'
};

// ==============================
// DATA MIGRATION
// ==============================
function migrateOldData() {
    if (!localStorage.getItem(STORAGE_KEYS.ORDERS) && localStorage.getItem('orders')) {
        localStorage.setItem(STORAGE_KEYS.ORDERS, localStorage.getItem('orders'));
    }
    if (!localStorage.getItem(STORAGE_KEYS.AGENTS) && localStorage.getItem('delegates')) {
        localStorage.setItem(STORAGE_KEYS.AGENTS, localStorage.getItem('delegates'));
    }
    if (!localStorage.getItem(STORAGE_KEYS.TRANSACTIONS) && localStorage.getItem('collectionTransactions')) {
        localStorage.setItem(STORAGE_KEYS.TRANSACTIONS, localStorage.getItem('collectionTransactions'));
    }
}
migrateOldData();

// ==============================
// DATA ACCESS
// ==============================
function getOrders() { return JSON.parse(localStorage.getItem(STORAGE_KEYS.ORDERS) || '[]'); }
function saveOrders(orders) { localStorage.setItem(STORAGE_KEYS.ORDERS, JSON.stringify(orders)); }
function getDelegates() { return JSON.parse(localStorage.getItem(STORAGE_KEYS.AGENTS) || '[]'); }
function saveDelegates(delegates) { localStorage.setItem(STORAGE_KEYS.AGENTS, JSON.stringify(delegates)); }
function getTransactions() { return JSON.parse(localStorage.getItem(STORAGE_KEYS.TRANSACTIONS) || '[]'); }
function saveTransactions(transactions) { localStorage.setItem(STORAGE_KEYS.TRANSACTIONS, JSON.stringify(transactions)); }

// ==============================
// ORDER STATUS UPDATE
// ==============================
function updateOrderStatus(orderId, newStatus, extra = {}) {
    const orders = getOrders();
    const idx = orders.findIndex(o => o.id == orderId);
    if (idx === -1) return false;
    orders[idx].status = newStatus;
    orders[idx].statusHistory = orders[idx].statusHistory || [];
    orders[idx].statusHistory.push({
        status: newStatus,
        timestamp: Date.now(),
        note: extra.note || '',
        delegateId: extra.delegateId || orders[idx].delegateId
    });
    if (extra.delegateId) orders[idx].delegateId = extra.delegateId;
    if (newStatus === 'assigned') orders[idx].assignedAt = Date.now();
    if (newStatus === 'rejected') orders[idx].rejectedAt = Date.now();
    if (newStatus === 'approved_by_company') orders[idx].approvedAt = Date.now();
    saveOrders(orders);
    return true;
}

// ==============================
// COMPANY FINANCIALS
// ==============================
function getCompanyExpectedCash() {
    const orders = getOrders();
    return orders.reduce((sum, o) => {
        if (o.status === 'delivered_pending_company_approval' || o.status === 'approved_by_company') {
            return sum + (o.netAmount || 0);
        }
        return sum;
    }, 0);
}
function getCompanySubmittedCash() {
    const transactions = getTransactions();
    return transactions.reduce((sum, t) => sum + (t.amount || 0), 0);
}
function getCompanyBalance() {
    return getCompanyExpectedCash() - getCompanySubmittedCash();
}

// ==============================
// TOAST
// ==============================
function showToast(message, type = 'info') {
    let container = document.querySelector('.company-toast-container');
    if (!container) {
        container = document.createElement('div');
        container.className = 'company-toast-container';
        document.body.appendChild(container);
    }
    const toast = document.createElement('div');
    toast.className = `company-toast company-toast-${type}`;
    const icon = type === 'success' ? 'fa-circle-check' : (type === 'error' ? 'fa-circle-exclamation' : 'fa-info-circle');
    toast.innerHTML = `<i class="fa-solid ${icon}"></i> <span>${message}</span>`;
    container.appendChild(toast);
    setTimeout(() => toast.remove(), 3000);
}

