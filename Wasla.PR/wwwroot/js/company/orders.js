window.renderDelegatesList = function() {
    const container = document.querySelector('.company-delegates-grid');
    if (!container) return;
    const delegates = getDelegates();
    if (delegates.length === 0) {
        container.innerHTML = '<div class="company-card" style="text-align:center;">لا يوجد مناديب</div>';
        return;
    }
    container.innerHTML = delegates.map(delegate => `
        <div class="company-delegate-card" data-id="${delegate.id}">
            <div class="company-delegate-header">
                <img src="${delegate.avatar || 'https://ui-avatars.com/api/?name=' + encodeURIComponent(delegate.name) + '&background=f0f7ff&color=0061ff'}" class="company-avatar">
                <div>
                    <h3>${delegate.name}</h3>
                    <span class="company-muted">${delegate.phone}</span>
                </div>
                <span class="company-badge ${delegate.status === 'active' ? 'company-badge-success' : 'company-badge-danger'}">${delegate.status === 'active' ? 'نشط' : 'غير نشط'}</span>
            </div>
            <div class="company-delegate-info">
                <div><span><i class="fa-solid fa-box"></i> عدد الطلبات</span><h4>${getOrders().filter(o => o.delegateId === delegate.id).length}</h4></div>
            </div>
            <div class="company-delegate-actions">
                <a href="delegate-details.html?id=${delegate.id}" class="company-btn company-btn-primary"><i class="fa-solid fa-eye"></i> تفاصيل</a>
                <button class="company-btn company-btn-primary assign-order-btn" data-id="${delegate.id}"><i class="fa-solid fa-user-plus"></i> إسناد طلب</button>
                <button class="company-btn company-btn-danger delete-delegate" data-id="${delegate.id}"><i class="fa-solid fa-trash"></i></button>
            </div>
        </div>
    `).join('');
    // Bind assign order buttons
    document.querySelectorAll('.assign-order-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const id = btn.dataset.id;
            window.showAssignOrderToDelegate(id);
        });
    });
    // Bind delete buttons
    document.querySelectorAll('.delete-delegate').forEach(btn => {
        btn.addEventListener('click', async (e) => {
            const id = btn.dataset.id;
            const delegate = getDelegates().find(d => d.id === id);
            const balance = (delegate.cashInHand || 0) - (delegate.cashDeliveredToCompany || 0);
            const activeOrders = getOrders().filter(o => o.delegateId === id && (o.status === 'assigned' || o.status === 'picked_up')).length;
            if (balance !== 0 || activeOrders > 0) {
                showToast('لا يمكن حذف المندوب: لديه رصيد غير صفري أو طلبات نشطة', 'error');
                return;
            }
            const confirmed = await window.showConfirmModal('هل أنت متأكد من حذف هذا المندوب؟', 'تأكيد الحذف');
            if (confirmed) {
                const newDelegates = getDelegates().filter(d => d.id !== id);
                saveDelegates(newDelegates);
                window.renderDelegatesList();
                showToast('تم حذف المندوب بنجاح', 'success');
            }
        });
    });
};

