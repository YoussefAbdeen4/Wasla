let currentLang = localStorage.getItem('appLang') || 'ar'; // Default to Arabic for public pages reference

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if(btn.dataset.lang === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Translate elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if(window.translations[lang] && window.translations[lang][key]) {
            if(el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.placeholder) el.placeholder = window.translations[lang][key];
                else el.value = window.translations[lang][key];
            } else {
                el.textContent = window.translations[lang][key];
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Initialize Language
    setLanguage(currentLang);
    
    // Setup language switcher listeners
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.target.dataset.lang;
            if (lang) {
                setLanguage(lang);
            }
        });
    });

    // ---------------------------------------------------------
    // PREMIUM MOTION & UI EXPERIENCE (Intersection Observer)
    // ---------------------------------------------------------
    const motionObserverOptions = {
        root: null,
        rootMargin: '0px 0px -50px 0px',
        threshold: 0.15
    };

    const motionObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                // Add active class to trigger CSS transition
                entry.target.classList.add('active');
                
                // If it's a counter, start the number animation
                if (entry.target.classList.contains('counter-value')) {
                    startCounterAnimation(entry.target);
                }
                
                // Animate once per page load to avoid spam
                observer.unobserve(entry.target);
            }
        });
    }, motionObserverOptions);

    // Observe all reveal elements
    document.querySelectorAll('.reveal, .counter-value').forEach(el => {
        motionObserver.observe(el);
    });

    function startCounterAnimation(element) {
        const target = parseInt(element.getAttribute('data-target'), 10);
        if (isNaN(target)) return;
        
        const duration = 2000; // 2 seconds
        const frameRate = 1000 / 60; // 60fps
        const totalFrames = Math.round(duration / frameRate);
        let frame = 0;

        // easeOutQuart
        const easeOutQuart = t => 1 - (--t) * t * t * t;

        const counterInterval = setInterval(() => {
            frame++;
            const progress = easeOutQuart(frame / totalFrames);
            const current = Math.round(target * progress);

            element.textContent = current.toLocaleString();

            if (frame === totalFrames) {
                clearInterval(counterInterval);
                element.textContent = target.toLocaleString();
            }
        }, frameRate);
    }
    // ---------------------------------------------------------
});
